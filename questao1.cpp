#include <stdio.h> // cabeçalho que da entrada e saida 


int main(){ // funcao principal 
	
	int n1,n2,soma; // variaveis do tipo inteiro
	
	printf("Digite o primeiro numero: "); // imprimindo msg na tela pedindo o 1 numero
	scanf("%d",&n1); // guardando o numero 1
	printf("Digite o segundo numero: "); // imprimindo msg na tela pedindo o 2 numero
	scanf("%d",&n2); // guardando o numero 2
	soma = n1+n2; // criando a variavel inteira soma que soma os valores que o usuario digitou
	printf("Essa e a soma dos dois numeros: %d",soma); // imprimindo a soma dos numeros
	
	
	
	
}
